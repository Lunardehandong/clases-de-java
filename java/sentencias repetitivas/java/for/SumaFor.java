public class SumaFor {
    public static void main(String[] args) {
        int suma = 0;

        // Usando for para sumar los primeros 5 números naturales
        for (int i = 1; i <= 5; i++) {
            suma += i; // Acumular la suma
        }

        System.out.println("La suma de los primeros 5 números es: " + suma);
    }
}
